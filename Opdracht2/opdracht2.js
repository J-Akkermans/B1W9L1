// Addition function.
function addition(num1, num2) {
    return num1 + num2;
}
var answerAdd = addition(10, 12);
console.log("addition " + answerAdd)


// Substraction function.
function subtraction(num1, num2) {
    return num1 - num2;
}
var answerSub = subtraction(58, 34);
console.log("subtraction " + answerSub)


// Multiplication function
function multiplication(num1, num2) {
    return num1 * num2;
}
var answerMul = multiplication(6, 7);
console.log("multiplication " + answerMul)


// Multiplication function
function division(num1, num2) {
    return num1 / num2;
}
var answerDiv = division(144, 12);
console.log("division " + answerDiv)


// Increment function
function increment(num) {
    return ++num
}
var answerInc = increment(12);
console.log("increment " + answerInc)


// Decrement function
function decrement(num) {
    return --num
}
var answerDec = decrement(34);
console.log("decrement " + answerDec)